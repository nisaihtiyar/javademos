public class Main {
    public static void main(String[] args) {
       //for dongusu
        for(int i=0;i<10;i++){
            System.out.println(i+" helloo");
        }

        //while dongusu
        int i=0;
        while(i<10){
            System.out.println(i+" hi");
            i++;
        }

        //do-while dongusu
        int j=0;
        do{
            System.out.println(j+" hell");
            j+=2;
        }while(j<=10);
    }
}